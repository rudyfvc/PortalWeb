package com.portal.dto;

public class TipoEntidadDTO {
	
	private String cod_tipo_entidad;
	private String tipo_entidad;
	private String estado;
	
	
	public String getCod_tipo_entidad() {
		return cod_tipo_entidad;
	}
	public void setCod_tipo_entidad(String cod_tipo_entidad) {
		this.cod_tipo_entidad = cod_tipo_entidad;
	}
	public String getTipo_entidad() {
		return tipo_entidad;
	}
	public void setTipo_entidad(String tipo_entidad) {
		this.tipo_entidad = tipo_entidad;
	}
	public String getEstado() {
		return estado;
	}
	public void setEstado(String estado) {
		this.estado = estado;
	}
	
	

}
