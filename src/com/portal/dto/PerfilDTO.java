package com.portal.dto;

public class PerfilDTO {

	private String app_nombre;
	private String cod_perfil;
	private String per_nombre;
	private String per_estado;
	
	
	public String getApp_nombre() {
		return app_nombre;
	}
	public void setApp_nombre(String app_nombre) {
		this.app_nombre = app_nombre;
	}
	public String getCod_perfil() {
		return cod_perfil;
	}
	public void setCod_perfil(String cod_perfil) {
		this.cod_perfil = cod_perfil;
	}
	public String getPer_nombre() {
		return per_nombre;
	}
	public void setPer_nombre(String per_nombre) {
		this.per_nombre = per_nombre;
	}
	public String getPer_estado() {
		return per_estado;
	}
	public void setPer_estado(String per_estado) {
		this.per_estado = per_estado;
	}
	
	
	
	
	
}
